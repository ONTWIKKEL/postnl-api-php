namespace Firstred\PostNL\Entity;

use Firstred\PostNL\Misc\ValidateAndFix;
use libphonenumber\Leniency\Valid;
use ReflectionException;
use TypeError;

/**
 * Class Shipment
 */
class Shipment extends AbstractEntity
{
    /**
     * Addresses
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var Address[]|null $addresses
     *
     * @since 1.0.0
     */
    protected $addresses;

    /**
     * Amounts
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var Amount[]|null $amounts
     *
     * @since 1.0.0
     */
    protected $amounts;

    /**
     * GenerateBarcode
     *
     * @pattern ^.{1,35}$
     *
     * @example 3SDEVC2016112104
     *
     * @var string|null $barcode
     *
     * @since 1.0.0
     */
    protected $barcode;

    /**
     * Code used for logistic purposes (usually generated by the service itself when needed)
     *
     * @pattern ^.{0,35}$
     *
     * @example #2426A3A#03#0306#
     *
     * @var string|null $codingText
     *
     * @since 1.0.0
     */
    protected $codingText;

    /**
     * Ending date/time of the collection of the shipment. Format: d-m-Y H:i:s
     *
     * @pattern ^(?:0[1-9]|[1-2][0-9]|3[0-1])-(?:0[1-9]|1[0-2])-[0-9]{4}\s(?:2[0-3]|[01][0-9]):[0-5][0-9]:[0-5][0-9]$
     *
     * @example 03-07-2019 17:00:00
     *
     * @var string|null $collectionTimeStampEnd
     *
     * @since 1.0.0
     */
    protected $collectionTimeStampEnd;

    /**
     * Starting date/time of the collection of the shipment. Format: d-m-Y H:i:s
     *
     * @pattern ^(?:[0-3]\d-[01]\d-[12]\d{3}\s+)[0-2]\d:[0-5]\d(?:[0-5]\d)$
     *
     * @example 03-07-2019 16:00:00
     *
     * @var string|null $collectionTimeStampStart
     *
     * @since 1.0.0
     */
    protected $collectionTimeStampStart;

    /**
     * One or more Contact elements belonging to a shipment. Mandatory in some cases. Please refer to the Guidelines.
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var Contact[]|null $contacts
     *
     * @since 1.0.0
     */
    protected $contacts;

    /**
     * Content of the shipment. Mandatory for Extra@Home shipments
     *
     * @pattern ^.{0,35}$
     *
     * @example Media player
     *
     * @var string|null $content
     *
     * @since 1.0.0
     */
    protected $content;

    /**
     * Cost center of the shipment.
     * This value will appear on your invoice.
     *
     * @pattern ^.{0,35}$
     *
     * @example SX-GT-66
     *
     * @var string|null $costCenter
     *
     * @since 1.0.0
     */
    protected $costCenter;

    /**
     * Order number of the customer.
     *
     * @pattern ^.{0,35}$
     *
     * @example 8689242390
     *
     * @var string|null $customerOrderNumber
     *
     * @since 1.0.0
     */
    protected $customerOrderNumber;

    /**
     * The Customs type is mandatory for GlobalPack shipments and not allowed for any other shipment types.
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var Customs|null $customs
     *
     * @since 1.0.0
     */
    protected $customs;

    /**
     * Delivery address type
     *
     * @pattern pattern: ^\d{2}$
     *
     * @example 09
     *
     * @var string|null $deliveryAddress
     *
     * @since 1.0.0
     */
    protected $deliveryAddress;

    /**
     * The delivery date and the specific starting time of the shipment delivery. This can be retrieved from the DeliveryDate webservice using the MyTime delivery option. Format: d-m-Y H:i:s
     *
     * @pattern ^(?:[0-3]\d-[01]\d-[12]\d{3}\s+)[0-2]\d:[0-5]\d(?:[0-5]\d)$
     *
     * @example 03-07-2019 14:30:00
     *
     * @var string|null $deliveryTimeStampStart
     *
     * @since 1.0.0
     */
    protected $deliveryTimeStampStart;

    /**
     * The delivery date and the specific end time of the shipment delivery. This can be retrieved from the DeliveryDate webservice using the MyTime delivery option. Format: d-m-Y H:i:s
     *
     * @pattern ^(?:[0-3]\d-[01]\d-[12]\d{3}\s+)[0-2]\d:[0-5]\d(?:[0-5]\d)$
     *
     * @example 03-07-2019 16:30:00
     *
     * @var string|null $deliveryTimeStampEnd
     *
     * @since 1.0.0
     */
    protected $deliveryTimeStampEnd;

    /**
     * The delivery date of the shipment. We strongly advice to use the DeliveryDate service to get this date when using delivery options like Evening/Morning/Sameday delivery etc. For those products, this field is mandatory (please regard the Guidelines section). Format: d-m-Y H:i:s
     *
     * @pattern ^(?:[0-3]\d-[01]\d-[12]\d{3}\s+)[0-2]\d:[0-5]\d(?:[0-5]\d)$
     *
     * @example 03-07-2019 14:00:00
     *
     * @var string|null $deliveryDate
     *
     * @since 1.0.0
     */
    protected $deliveryDate;

    /**
     * Dimension
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var Dimension|null $dimension
     *
     * @since 1.0.0
     */
    protected $dimension;

    /**
     * GenerateBarcode of the downstream network partner of PostNL Pakketten.
     * Madatory for requesting GlobalPack combilabel product codes
     *
     * @pattern ^.{0,35}$
     *
     * @example CD123456785NL
     *
     * @var string|null $downPartnerBarcode
     *
     * @since 1.0.0
     */
    protected $downPartnerBarcode;

    /**
     * Identification of the downstream network partner of PostNL Pakketten.
     * Mandatory for Pickup at PostNl Location Belgium. Use the data from the Location API response; attribute RetailNetworkID
     *
     * @pattern ^.{0,35}$
     *
     * @example LD-01
     *
     * @var string|null $downPartnerID
     *
     * @since 1.0.0
     */
    protected $downPartnerID;

    /**
     * Identification of the location of the downstream network partner of PostNL Pakketten.
     *
     * @pattern ^.{0,10}$
     *
     * @example BE0Q82
     *
     * @var string|null $downPartnerLocation
     *
     * @since 1.0.0
     */
    protected $downPartnerLocation;

    /**
     * List of 0 or more Group types with data, grouping multiple shipments together.
     * Mandatory for multicollo shipments. Please see Guidelines (Multiple shipments) for more information.
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var Group[]|null $groups
     *
     * @since 1.0.0
     */
    protected $groups;

    /**
     * Expiration date of the ID document.
     * Mandatory for ID Check products
     *
     * @pattern  ^(?:[0-3]\d-[01]\d-[12]\d{3})$
     *
     * @example  05-07-2019
     *
     * @var string|null $IDExpiration
     *
     * @since 1.0.0
     */
    protected $IDExpiration;

    /**
     * Document number of the ID document
     * Mandatory for ID Check products
     *
     * @pattern ^.{0,20}$
     *
     * @example 4261103214
     *
     * @var string|null $IDNumber
     *
     * @since 1.0.0
     */
    protected $IDNumber;

    /**
     * Type of the ID document.
     * Mandatory for ID Check products.
     *
     * @pattern ^\d{2}$
     *
     * @example 02
     *
     * @var string|null $IDType
     *
     * @since 1.0.0
     */
    protected $IDType;

    /**
     * Second product code of a shipment
     *
     * @pattern ^\d{4}$
     *
     * @example 3153
     *
     * @var string|null $productCodeCollect
     *
     * @since 1.0.0
     */
    protected $productCodeCollect;

    /**
     * Product code of the shipment
     *
     * @pattern ^\d{4}$
     *
     * @example 3085
     *
     * @var string|null $productCodeDelivery
     *
     * @since 1.0.0
     */
    protected $productCodeDelivery;

    /**
     * Product options for the shipment, mandatory for certain products, see the Products page of this webservice
     *
     * @pattern N/A
     *
     * @example N/A
     *
     * @var ProductOption[]|null $productOptions
     *
     * @since 1.0.0
     */
    protected $productOptions;

    /**
     * Date of birth
     * Mandatory for Age check products
     *
     * @pattern  ^(?:[0-3]\d-[01]\d-[12]\d{3})$
     *
     * @example  01-01-1970
     *
     * @var string|null $receiverDateOfBirth
     *
     * @since 1.0.0
     */
    protected $receiverDateOfBirth;

    /**
     * Your own reference of the shipment. Mandatory for Extra@Home shipments; for E@H this is used to create your order number, so this should be unique for each request.
     *
     * @pattern ^.{0,35}$
     *
     * @example 2016014567
     *
     * @var string|null $reference
     *
     * @since 1.0.0
     */
    protected $reference;

    /**
     * Additional reference of the collect order of the shipment.
     *
     * @pattern ^.{0,35}$
     *
     * @example 6659150
     *
     * @var string|null $referenceCollect
     *
     * @since 1.0.0
     */
    protected $referenceCollect;

    /**
     * Remark of the shipment.
     *
     * @pattern ^.{0,35}$
     *
     * @example Fragile
     *
     * @var string|null $remark
     *
     * @since 1.0.0
     */
    protected $remark;

    /**
     * Return barcode of the shipment. PostNL will provide you with a separate customer code specifically for generating the returnBarcode.
     *
     * Mandatory for Label in the Box (return label) shipments.
     *
     * @pattern ^.{11,15}$
     *
     * @example 3SABCD7762162
     *
     * @var string|null $returnBarcode
     *
     * @since 1.0.0
     */
    protected $returnBarcode;

    /**
     * Return reference of the shipment.
     *
     * @pattern ^.{0,35}$
     *
     * @example 112233
     *
     * @var string|null $returnReference
     *
     * @since 1.0.0
     */
    protected $returnReference;

    /**
     * Shipment constructor.
     *
     * @param Address[]|null       $addresses
     * @param array|null           $amounts
     * @param string|null          $barcode
     * @param Contact[]|null       $contacts
     * @param string|null          $content
     * @param string|null          $collectionTimeStampEnd
     * @param string|null          $collectionTimeStampStart
     * @param string|null          $costCenter
     * @param string|null          $customerOrderNumber
     * @param Customs|null         $customs
     * @param string|null          $deliveryAddress
     * @param string|null          $deliveryDate
     * @param Dimension|null       $dimension
     * @param string|null          $downPartnerBarcode
     * @param string|null          $downPartnerId
     * @param string|null          $downPartnerLocation
     * @param Group[]|null         $groups
     * @param string|null          $idExpiration
     * @param string|null          $idNumber
     * @param string|null          $idType
     * @param string|null          $productCodeCollect
     * @param string|null          $productCodeDelivery
     * @param ProductOption[]|null $productOptions
     * @param string|null          $receiverDateOfBirth
     * @param string|null          $reference
     * @param string|null          $referenceCollect
     * @param string|null          $remark
     * @param string|null          $returnBarcode
     * @param string|null          $returnReference
     *
     * @throws TypeError
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     */
    public function __construct(?array $addresses = null, ?array $amounts = null, ?string $barcode = null, ?array $contacts = null, ?string $content = null, ?string $collectionTimeStampEnd = null, ?string $collectionTimeStampStart = null, ?string $costCenter = null, ?string $customerOrderNumber = null, ?Customs $customs = null, ?string $deliveryAddress = null, ?string $deliveryDate = null, ?Dimension $dimension = null, ?string $downPartnerBarcode = null, ?string $downPartnerId = null, ?string $downPartnerLocation = null, ?array $groups = null, ?string $idExpiration = null, ?string $idNumber = null, ?string $idType = null, ?string $productCodeCollect = null, ?string $productCodeDelivery = null, ?array $productOptions = null, ?string $receiverDateOfBirth = null, ?string $reference = null, ?string $referenceCollect = null, ?string $remark = null, ?string $returnBarcode = null, ?string $returnReference = null)
    {
        parent::__construct();

        $this->setAddresses($addresses);
        $this->setAmounts($amounts);
        $this->setBarcode($barcode);
        $this->setCollectionTimeStampEnd($collectionTimeStampEnd);
        $this->setCollectionTimeStampStart($collectionTimeStampStart);
        $this->setContacts($contacts);
        $this->setContent($content);
        $this->setCostCenter($costCenter);
        $this->setCustomerOrderNumber($customerOrderNumber);
        $this->setCustoms($customs);
        $this->setDeliveryAddress($deliveryAddress);
        $this->setDeliveryDate($deliveryDate);
        $this->setDimension($dimension);
        $this->setDownPartnerBarcode($downPartnerBarcode);
        $this->setDownPartnerID($downPartnerId);
        $this->setDownPartnerLocation($downPartnerLocation);
        $this->setGroups($groups);
        $this->setIDExpiration($idExpiration);
        $this->setIDNumber($idNumber);
        $this->setIDType($idType);
        $this->setProductCodeCollect($productCodeCollect);
        $this->setProductCodeDelivery($productCodeDelivery);
        $this->setProductOptions($productOptions);
        $this->setReceiverDateOfBirth($receiverDateOfBirth);
        $this->setReference($reference);
        $this->setReferenceCollect($referenceCollect);
        $this->setRemark($remark);
        $this->setReturnBarcode($returnBarcode);
        $this->setReturnReference($returnReference);
    }

    /**
     * Get addresses
     *
     * @return Address[]|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$addresses
     */
    public function getAddresses(): ?array
    {
        return $this->addresses;
    }

    /**
     * Set addresses
     *
     * @pattern N/A
     *
     * @param Address[]|null $addresses
     *
     * @return static
     *
     * @throws TypeError
     *
     * @example N/A
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$addresses
     */
    public function setAddresses(?array $addresses): Shipment
    {
        $this->addresses = $addresses;

        return $this;
    }

    /**
     * Get amounts
     *
     * @return Amount[]|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$amounts
     */
    public function getAmounts(): ?array
    {
        return $this->amounts;
    }

    /**
     * Set amounts
     *
     * @pattern N/A
     *
     * @param Amount[]|null $amounts
     *
     * @return static
     *
     * @throws TypeError
     *
     * @example N/A
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$amounts
     */
    public function setAmounts(?array $amounts): Shipment
    {
        $this->amounts = $amounts;

        return $this;
    }

    /**
     * Get barcode
     *
     * @return string|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$barcode
     */
    public function getBarcode(): ?string
    {
        return $this->barcode;
    }

    /**
     * Set barcode
     *
     * @pattern ^.{1,35}$
     *
     * @param string|null $barcode
     *
     * @return static
     *
     * @throws TypeError
     * @throws ReflectionException
     *
     * @example 3SDEVC2016112104
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$barcode
     */
    public function setBarcode(?string $barcode): Shipment
    {
        $this->barcode = ValidateAndFix::barcode($barcode);

        return $this;
    }

    /**
     * Get coding text
     *
     * @return string|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$codingText
     */
    public function getCodingText(): ?string
    {
        return $this->codingText;
    }

    /**
     * Set coding text
     *
     * @pattern ^.{0,35}$
     *
     * @example #2426A3A#03#0306#
     *
     * @param string|null $codingText
     *
     * @return static
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$codingText
     *
     * @throws TypeError
     * @throws ReflectionException
     */
    public function setCodingText(?string $codingText): Shipment
    {
        $this->codingText = ValidateAndFix::genericString($codingText);

        return $this;
    }

    /**
     * Get collection timestamp end
     *
     * @return string|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$collectionTimeStampEnd
     */
    public function getCollectionTimeStampEnd(): ?string
    {
        return $this->collectionTimeStampEnd;
    }

    /**
     * Set collection timestamp end
     *
     * @pattern ^(?:[0-3]\d-[01]\d-[12]\d{3}\s+)[0-2]\d:[0-5]\d(?:[0-5]\d)$
     *
     * @param string|null $collectionTimeStampEnd
     *
     * @return static
     *
     * @throws TypeError
     * @throws ReflectionException
     *
     * @example 03-07-2019 17:00:00
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$collectionTimeStampEnd
     */
    public function setCollectionTimeStampEnd(?string $collectionTimeStampEnd): Shipment
    {
        $this->collectionTimeStampEnd = ValidateAndFix::dateTime($collectionTimeStampEnd);

        return $this;
    }

    /**
     * Get collection timestamp start
     *
     * @return string|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$collectionTimeStampStart
     */
    public function getCollectionTimeStampStart(): ?string
    {
        return $this->collectionTimeStampStart;
    }

    /**
     * Set collection timestamp start
     *
     * @pattern ^(?:[0-3]\d-[01]\d-[12]\d{3}\s+)[0-2]\d:[0-5]\d(?:[0-5]\d)$
     *
     * @param string|null $collectionTimeStampStart
     *
     * @return static
     *
     * @throws TypeError
     * @throws ReflectionException
     *
     * @example 03-07-2019 16:00:00
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$collectionTimeStampStart
     */
    public function setCollectionTimeStampStart(?string $collectionTimeStampStart): Shipment
    {
        $this->collectionTimeStampStart = ValidateAndFix::dateTime($collectionTimeStampStart);

        return $this;
    }

    /**
     * Get contacts
     *
     * @pattern N/A
     *
     * @return Contact[]|null
     *
     * @example N/A
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$contacts
     */
    public function getContacts(): ?array
    {
        return $this->contacts;
    }

    /**
     * Set contacts
     *
     * @pattern N/A
     *
     * @param Contact[]|null $contacts
     *
     * @return static
     *
     * @throws TypeError
     *
     * @example N/A
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$contacts
     */
    public function setContacts(?array $contacts): Shipment
    {
        $this->contacts = $contacts;

        return $this;
    }

    /**
     * Get content
     *
     * @pattern N/A
     *
     * @return string|null
     *
     * @example N/A
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$content
     */
    public function getContent(): ?string
    {
        return $this->content;
    }

    /**
     * Set content
     *
     * @pattern N/A
     *
     * @param string|null $content
     *
     * @return static
     *
     * @throws TypeError
     *
     * @example N/A
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$content
     */
    public function setContent(?string $content): Shipment
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get cost center
     *
     * @return string|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$costCenter
     */
    public function getCostCenter(): ?string
    {
        return $this->costCenter;
    }

    /**
     * Set cost center
     *
     * @pattern ^.{0,35}$
     *
     * @param string|null $costCenter
     *
     * @return static
     *
     * @throws TypeError
     * @throws ReflectionException
     *
     * @example SX-GT-66
     *
     * @since   1.0.0
     * @since   2.0.0 Strict typing
     *
     * @see     Shipment::$costCenter
     */
    public function setCostCenter(?string $costCenter): Shipment
    {
        $this->costCenter = ValidateAndFix::genericString($costCenter);

        return $this;
    }

    /**
     * Get customer order number
     *
     * @return string|null
     *
     * @since 1.0.0
     * @since 2.0.0 Strict typing
     *
     * @see   Shipment::$customerOrderNumber
     */
    public function getCustomerOrderNumber(): ?string
    {
        return $this->customerOrderNumber;
    }

    /**
     * Set customer order number
     *
     * @pattern ^.{0,35}$
     *
     * @param string|null $customerOrderNumber
     *
     * @return static
     *
     * @throws TypeError
     * @throws ReflectionException
     *
     * @example 8689242390
     *
